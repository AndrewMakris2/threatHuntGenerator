// ── CSV Export ─────────────────────────────────────────────────────────────────

export function exportHuntsAsCSV(hunts, filename = 'phantom-hunts') {
  const headers = [
    'ID', 'Title', 'Category', 'Severity', 'Confidence', 'Relevance Score',
    'Difficulty', 'Estimated Time', 'MITRE Techniques', 'Tags',
  ];

  const rows = hunts.map(h => [
    h.id,
    `"${(h.title || '').replace(/"/g, '""')}"`,
    h.category,
    h.severity,
    h.confidence,
    h.relevanceScore,
    h.difficulty,
    h.estimatedTime,
    `"${(h.mitreTechniques || []).join(', ')}"`,
    `"${(h.tags || []).join(', ')}"`,
  ]);

  const csv = [headers.join(','), ...rows.map(r => r.join(','))].join('\n');
  const blob = new Blob([csv], { type: 'text/csv' });
  const url = URL.createObjectURL(blob);
  triggerDownload(url, `${filename}-${formatDate()}.csv`);
  URL.revokeObjectURL(url);
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function triggerDownload(url, filename) {
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
}

function formatDate() {
  return new Date().toISOString().split('T')[0];
}

function sanitizeFilename(str) {
  return (str || 'hunt').replace(/[^a-z0-9]/gi, '-').toLowerCase().slice(0, 40);
}

function formatHuntAsText(hunt) {
  return [
    `THREAT HUNT: ${hunt.title}`,
    `${'='.repeat(60)}`,
    '',
    `Category: ${hunt.category} | Severity: ${hunt.severity} | Confidence: ${hunt.confidence}%`,
    '',
    'WHY RELEVANT',
    hunt.whyRelevant,
    '',
    'HYPOTHESIS',
    hunt.hypothesis,
    '',
    'MITRE TECHNIQUES',
    (hunt.mitreTechniques || []).join(', '),
    '',
    'HUNT STEPS',
    (hunt.huntSteps || []).map((s, i) => `${i + 1}. ${s}`).join('\n'),
    '',
    'SUSPICIOUS BEHAVIORS',
    (hunt.suspiciousBehaviors || []).map(s => `• ${s}`).join('\n'),
    '',
    'TRIAGE GUIDANCE',
    hunt.triageGuidance,
  ].join('\n');
}

// ── Severity color helpers ─────────────────────────────────────────────────────

function severityColor(s) {
  return { critical: '#ef4444', high: '#f97316', medium: '#eab308', low: '#22c55e' }[s] || '#6b7280';
}

function severityBg(s) {
  return { critical: 'rgba(239,68,68,0.15)', high: 'rgba(249,115,22,0.15)', medium: 'rgba(234,179,8,0.15)', low: 'rgba(34,197,94,0.15)' }[s] || 'rgba(107,114,128,0.15)';
}

